# this is when to use hard brackets [] 
# a hard bracket is used to make a list
myList = [4, True, 'girl', 4.0]
# hard brackets are also used to call an index of a data set
print(myList[2]) 

myDictionary = {"integer": 4, "boolean": True, "string": "girl", "float": 4.0}
# print out the value of the first key in myDictionary using its index
print(myDictionary["integer"])

print(f"this is how we call in our variables {myList} and {myDictionary}.")